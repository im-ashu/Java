  public char[][] updateBoard(char[][] board, int[] click) {
    if (board[click[0]][click[1]] == 'M') {
      board[click[0]][click[1]] = 'X';
      return board;
    }
    int rows = board.length;
    int cols = board[0].length;
    boolean[][] visited = new boolean[rows][cols];
    Queue<int[]> q = new LinkedList<>();
    q.add(new int[]{click[0], click[1]});
    visited[click[0]][click[1]] = true;
    while (!q.isEmpty()) {
      int[] currentPoint = q.poll();
      int x = currentPoint[0];
      int y = currentPoint[1];
      int numBombs = getNumberOfBombs(board, x, y);
      if (numBombs > 0) {
        board[x][y] = (char) (numBombs + '0');
      } else {
        board[x][y] = 'B';
        for (int[] points : dir) {
          int nx = x + points[0];
          int ny = y + points[1];
          if (nx >= 0 && ny >= 0 && nx < board.length && ny < board[0].length
              && !visited[nx][ny] && board[nx][ny] == 'E') {
            q.add(new int[]{nx, ny});
            visited[nx][ny] = true;
          }
        }

      }
    }
    return board;
  }

  private int getNumberOfBombs(char[][] board, int x, int y) {
    int count = 0;
    for (int[] points : dir) {
      int nx = x + points[0];
      int ny = y + points[1];
      if (nx >= 0 && ny >= 0 && nx < board.length && ny < board[0].length) {
        count += board[nx][ny] == 'M' ? 1 : 0;
      }
    }
    return count;
  }
